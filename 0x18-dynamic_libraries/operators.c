#include <stdio.h>

/**
 * add - Adds two integers
 * @x: The first integer
 * @y: The second integer
 * Return: The sum of the integers
 */

int add(int x, int y)
{
	return (x + y);
}

/**
 * sub - This subtracts two integers
 * @x: The first integer
 * @y: The second integer
 * Return: The subtracted result
 */

int sub(int x, int y)
{
	return (x - y);
}

/**
 * mul - This multiplies two integers
 * @x: The first integer
 * @y: The second integer
 * Return: The multiplied result
 */

int mul(int x, int y)
{
	return (x * y);
}

/**
 * div - This divides two integers
 * @x: The first integer
 * @y: The second integer
 * Return: The division result
 */

int div(int x, int y)
{
	return (x / y);
}

/**
 * mod - This carries out the mod of two integers
 * @x: The first integer
 * @y: The second integer
 * Return: The modulus result
 */

int mod(int x, int y)
{
	return (x % y);
}
